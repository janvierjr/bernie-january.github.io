////////////////////////////////////////////////////////////////////////////////

function drawChessboard(num) {
  let result = "";
  for (i = 0; i < num; i++) {
      for (v = 0; v < num; v++) {
      if ((i + v) % 2 == 0) {
        result += " ";
      } else {
        result += "#";
      }
    }
    result += "\n";
  }
  console.log(result);
}

